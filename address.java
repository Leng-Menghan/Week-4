public class address {
    int homeNo;
    String street;
    String village;
    String commune;
    String district;
    String province_city;

    public address(int homeNo, String street, String village, String commune, String district, String province_city) {
        this.homeNo = homeNo;
        this.street = street;
        this.village = village;
        this.commune = commune;
        this.district = district;
        this.province_city = province_city;
    }
}
